static void
keyboard_handle_key(void *data, struct wl_keyboard *keyboard,
                    uint32_t serial, uint32_t time, uint32_t key,
                    uint32_t state)
{
    if (state != WL_KEYBOARD_KEY_STATE_PRESSED) return;

    switch (key) {
        case KEY_Q:
            exit(0);
            break;
        case KEY_LEFT:
            view_rot[1] += 5.0;
            break;
        case KEY_RIGHT:
            view_rot[1] -= 5.0;
            break;
        case KEY_UP:
            view_rot[0] += 5.0;
            break;
        case KEY_DOWN:
            view_rot[0] -= 5.0;
            break;
    }
}

