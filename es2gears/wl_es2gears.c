/**
 * Main entry point for the Wayland GLES2 gears demo.
 *
 * @param argc argument count
 * @param argv argument vector
 * @return exit status
 */
int
main(int argc, char **argv)
{
    struct display display = {0};
    struct window window = { .display = &display, .width = 300, .height = 300 };
    char *wayland_name = NULL;

    for (int i = 1; i < argc; i++) {
        if (strcmp(argv[i], "-wayland") == 0 && i + 1 < argc) {
            wayland_name = argv[++i];
        } else if (strcmp(argv[i], "-info") == 0) {
            print_info = 1;
        } else if (strcmp(argv[i], "-fullscreen") == 0) {
            fullscreen = 1;
        } else if (strcmp(argv[i], "--help") == 0) {
            usage(argv[0]);
            return 0;
        } else {
            usage(argv[0]);
            return -1;
        }
    }

    display.display = wl_display_connect(wayland_name);
    if (display.display == NULL) {
        fprintf(stderr, "failed to connect to Wayland display\n");
        return -1;
    }
    display.registry = wl_display_get_registry(display.display);
    wl_registry_add_listener(display.registry, &registry_listener, &display);
    wl_display_roundtrip(display.display);

    init_egl(&display);
    create_window(&window);
    init_gl();

    struct timeval tv0;
    gettimeofday(&tv0, NULL);
    double t0 = tv0.tv_sec + tv0.tv_usec / 1000000.0;
    double t_last = t0;
    int frames = 0;

    while (wl_display_dispatch(display.display) != -1) {
        struct timeval tv;
        gettimeofday(&tv, NULL);
        double t = tv.tv_sec + tv.tv_usec / 1000000.0;
        double dt = t - t_last;
        t_last = t;

        angle += 70.0 * dt;
        if (angle > 3600.0)
           angle -= 3600.0;

        if (window.configured)
           redraw(&window);

        frames++;
        if (t - t0 >= 5.0) {
            GLfloat seconds = t - t0;
            printf("%d frames in %.1f seconds = %.3f FPS\n", frames, seconds, frames / seconds);
            t0 = t;
            frames = 0;
        }
    }

    return 0;
}
